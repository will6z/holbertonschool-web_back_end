#!/usr/bin/env python3
"""
This module provides a function `floor` that returns the floor of a float.
"""


import math

def floor(n: float) -> int:
    """
    Returns the floor of a float.

    Args:
        n (float): The number to floor.

    Returns:
        int: The floored value of the number.
    """
    return math.floor(n)
