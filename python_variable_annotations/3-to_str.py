#!/usr/bin/env python3
"""
This module provides a function `to_str` that returns the string representation of a float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Args:
        n (float): The number to convert to a string.

    Returns:
        str: The string representation of the number.
    """
    return str(n)
